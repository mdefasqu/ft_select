#include "libft.h"
#include <stdlib.h>

char	*ft_strdup(const char *s1)
{
	int		i;
	int		a;
	char	*dest;

	i = 0;
	if (s1 == NULL)
		return (NULL);
	a = ft_strlen(s1);
	dest = (char *)malloc(sizeof(char) * (a + 1));
	if (dest == NULL)
		return (NULL);
	while (s1[i] != '\0')
	{
		dest[i] = s1[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
